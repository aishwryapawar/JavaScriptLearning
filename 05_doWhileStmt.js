console.log(" =========================== Do While Statement =========================");
console.log("Print num 0 to 10");
var index=0;
do {
    console.log(index);
    index++;
    
} while (index<=10);

console.log(" print numbers from 70 to 7 decrement by 7 each number");
var a=70;
do {
    console.log(a);
    a=a-7;
} while (a>=7);